package com.example.android.roomwordssample

import androidx.room.Dao
import androidx.room.Insert
import androidx.room.OnConflictStrategy
import androidx.room.Query
import kotlinx.coroutines.flow.Flow

/**
 * The Room Magic is in this file, where you map a method call to an SQL query.
 *
 * When you are using complex data types, such as Date, you have to also supply type converters.
 * To keep this example basic, no types that require type converters are used.
 * See the documentation at
 * https://developer.android.com/topic/libraries/architecture/room.html#type-converters
 */

@Dao
interface WordDao {

    @Query("SELECT * FROM word_table")
    fun getAll(): Flow<List<Word>>

    @Insert(onConflict = OnConflictStrategy.IGNORE)
    suspend fun insert(word: Word)

    @Query("DELETE FROM word_table")
    suspend fun deleteAll()

    @Query("UPDATE word_table SET flag = 'done' WHERE id = :id")
    suspend fun undoneToDone(id: Long?)

    @Query("UPDATE word_table SET flag = 'undone' WHERE id = :id")
    suspend fun doneToUndone(id: Long?)

    @Query("SELECT * FROM word_table WHERE flag = 'undone'")
    fun getUndone(): Flow<List<Word>>
}
